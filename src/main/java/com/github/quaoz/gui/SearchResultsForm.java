	public static void setSearchResults(
		@NotNull ArrayList<Pair<Moth, Double>> searchResults
	) {
		ArrayList<Moth> moth = new ArrayList<>();
		searchResults.forEach(p -> moth.add(p.getKey()));
		SearchResultsForm.records = moth;
	}

